//the first way to write a message
Console.WriteLine("'Hello, World!' or 'Hello World!', it doesn't really matter!");

//the other way to do it
Console.Write("Congratulations!");
Console.Write(" ");
Console.Write("You wrote your first lines of code.");

//the difference here is that, one creates a full line when executed and the other just writes the content
Console.WriteLine(" ");
Console.WriteLine("Congratulations! ");
Console.Write("You wrote your first lines of code.");

